#!/bin/bash

# 仓储物资出入库动态管理系统 - 重启脚本
# 使用方法: ./scripts/restart.sh

echo "=========================================="
echo "  仓储物资出入库动态管理系统 - 重启脚本"
echo "=========================================="
echo ""

# 进入项目根目录
cd "$(dirname "$0")/.."

# 重启服务
echo ">>> 重启服务..."
docker-compose restart

# 等待服务启动
echo ""
echo ">>> 等待服务启动..."
sleep 5

# 检查服务状态
echo ""
echo ">>> 服务状态:"
docker-compose ps

echo ""
echo "=========================================="
echo "  系统重启完成!"
echo "=========================================="
